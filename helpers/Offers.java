package helpers;

public class Offers {

	public String product;
	public String supplierName;
	public float quantity;
	public float price;
	
	public Offers(String product, String supplierName, float quantity, float price) {
		this.product = product;
		this.supplierName = supplierName;
		this.quantity = quantity;
		this.price = price;
	}
	
}
